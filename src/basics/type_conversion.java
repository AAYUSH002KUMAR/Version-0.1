package basics;

import java.util.*;

public class type_conversion {

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        int a = 10;
        float b = a;

        System.out.println(b);

        double c = 9.78;
        int d = (int) c;

        System.out.println(d);

        sc.close();

    }

}
